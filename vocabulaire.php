				<h2>Portefeuille</h2>
				<p>Un portefeuille est un logiciel ou une application mobile qui va vous permettre de gérer vos crypto-actifs.
				</p>
			</div>
			<div>
				<h2>Minage</h2>
				<p>Le minage est le fait d'utiliser un appareil informatique (ordinateur, téléphone...) pour effectuer des calculs mathématiques, qui vont servir le réseau Bitcoin à confirmer les transactions et à augmenter la sécurité de celles-ci. </p>
			</div>
			<div>
				<h2>Cryptographie</h2>
				<p>La cryptographie est une des disciplines de la cryptologie s'attachant à protéger des messages (assurant confidentialité, authenticité et intégrité) en s'aidant souvent de secrets ou clés.
				</p>
			</div>
			<div>
				<h2>Blockchain</h2>
				<p>La blockchain est une technologie de stockage et de transmission d’informations, transparente, sécurisée, et fonctionnant sans organe central de contrôle (définition de blockchain France).
				</p>
			</div>
			<div>
				<h2>Crypto-devise</h2>
				<p>Crypto-devise est un autre nom pour la crypto-monnaie.</p>
